package projectOne;

public class Patient {

	String id;
	String gender;
	String bloodType;
	String weight;
	String hasVirus;
	
	public Patient(String id, String gender, String bloodType, String weight, String hasVirus){
		this.id = id;
		this.gender = gender;
		this.bloodType = bloodType;
		this.weight = weight;
		this.hasVirus = hasVirus;
	}

	
	
	
}
